import { hex2rgb } from '../src/io/hex/hex2rgb';

describe('hex2rgb()', () => {
  const tests = [
    // simple #rrggbb hex colors
    ['#000000', [0, 0, 0, 1]],
    ['#ffffff', [255, 255, 255, 1]],
    ['#ff0000', [255, 0, 0, 1]],
    ['#00ff00', [0, 255, 0, 1]],
    ['#0000ff', [0, 0, 255, 1]],
    ['#ffff00', [255, 255, 0, 1]],
    ['#00ffff', [0, 255, 255, 1]],
    ['#ff00ff', [255, 0, 255, 1]],

    // simple rrggbb hex colors without #
    ['000000', [0, 0, 0, 1]],
    ['ffffff', [255, 255, 255, 1]],
    ['ff0000', [255, 0, 0, 1]],
    ['00ff00', [0, 255, 0, 1]],
    ['0000ff', [0, 0, 255, 1]],
    ['ffff00', [255, 255, 0, 1]],
    ['00ffff', [0, 255, 255, 1]],
    ['ff00ff', [255, 0, 255, 1]],

    // simple shorthand hex colors
    ['#000', [0, 0, 0, 1]],
    ['#fff', [255, 255, 255, 1]],
    ['#f00', [255, 0, 0, 1]],
    ['#0f0', [0, 255, 0, 1]],
    ['#00f', [0, 0, 255, 1]],
    ['#ff0', [255, 255, 0, 1]],
    ['#0ff', [0, 255, 255, 1]],
    ['#f0f', [255, 0, 255, 1]],

    // simple shorthand hex colors without #
    ['000', [0, 0, 0, 1]],
    ['fff', [255, 255, 255, 1]],
    ['f00', [255, 0, 0, 1]],
    ['0f0', [0, 255, 0, 1]],
    ['00f', [0, 0, 255, 1]],
    ['ff0', [255, 255, 0, 1]],
    ['0ff', [0, 255, 255, 1]],
    ['f0f', [255, 0, 255, 1]],

    // different #rrggbbaa hex colors
    ['#00000000', [0, 0, 0, 0]],
    ['#ffffff80', [255, 255, 255, 0.5]],
    ['#ff000040', [255, 0, 0, 0.25]],
    ['#00FF00C0', [0, 255, 0, 0.75]],
    ['#FF00FFFF', [255, 0, 255, 1]],

    // different rrggbbaa hex colors without #
    ['00000000', [0, 0, 0, 0]],
    ['ffffff80', [255, 255, 255, 0.5]],
    ['ff000040', [255, 0, 0, 0.25]],
    ['00FF00C0', [0, 255, 0, 0.75]],
    ['FF00FFFF', [255, 0, 255, 1]],

    // different #rgba hex colors
    ['#0000', [0, 0, 0, 0]],
    ['#fff8', [255, 255, 255, 0.53]],
    ['#f004', [255, 0, 0, 0.27]],
    ['#0F0C', [0, 255, 0, 0.8]],
    ['#F0FF', [255, 0, 255, 1]],

    // different rgba hex colors without #
    ['0000', [0, 0, 0, 0]],
    ['fff8', [255, 255, 255, 0.53]],
    ['f004', [255, 0, 0, 0.27]],
    ['0F0C', [0, 255, 0, 0.8]],
    ['F0FF', [255, 0, 255, 1]],
  ];

  test.each(tests)('%s -> %j', (input, expected) => {
    expect(hex2rgb(input)).toStrictEqual(expected);
  });
});
